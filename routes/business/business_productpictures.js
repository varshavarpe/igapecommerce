var express = require("express");
var Business = require("../../models/business/business_productpictures");
const router = express.Router();

router.post("/save", async (req, res) => {
  let body = req.body;
  let business = new Business.Business();
  business.id = body.data.id;
  business.productid = body.data.productid;
  business.title = body.data.title;
  business.imagecode = body.data.imagecode;
  business.srno = body.data.srno;
  
  business.save().then(
    (result) => {
      let data = {
        data: {
          status: "success",
          data: result,
        },
      };
      res.end(JSON.stringify(data));
    },
    (err) => {
      let data = {
        data: {
          status: "fail",
        },
      };
      res.end(JSON.stringify(data));
    }
  );
});

